#include <iostream>
using namespace std;
int main()
{
    int n;
    cin>>n;
    if(n==10)
    {
        cout<<"number is equal to 10\n"<<endl;
    }
    else if(n>10)
    {
        cout<<"number is greater than 10 \n"<<endl;
    }
    else
    {
        cout<<"number is less than 10 \n"<<endl;
    }
    return 0;
}